
//Declarar dos variables numéricas (con el valor que desees), muestra por consola la suman,
//resta, multiplicación, división y módulo (resto de la división)

public class Ejercicio1 {
	public static void main(String[] args) {
		
		int x = 15, y = 10;
		System.out.println("La suma es: " + (x + y));
		System.out.println("La resta es: " + (x - y));
		System.out.println("La multiplicación es: " + (x * y));
		System.out.println("La división es: " + (x / y));
		System.out.println("El modulo es: " + (x % y));
	}
}
